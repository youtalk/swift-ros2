/// Converts a ``MessageIR`` into the Swift source text for a generated ``ROS2Message`` type.
public enum SwiftEmitter {
    /// Type names that collide with Swift stdlib types — these keep the `Msg` suffix so the
    /// generated symbol does not shadow the stdlib one. Anything else emits as bare PascalCase.
    private static let collisionTypeNames: Set<String> = [
        "Bool", "String",
        "Int", "Int8", "Int16", "Int32", "Int64",
        "UInt", "UInt8", "UInt16", "UInt32", "UInt64",
        "Float", "Float32", "Float64", "Double",
        "Empty",
    ]

    /// Render `ir` to Swift source.
    ///
    /// - Parameters:
    ///   - isNested: when `true`, drop `ROS2Message` conformance + the `typeInfo` constant.
    ///     Used for nested-only payloads (e.g. `Header`, `Vector3`, `UUID`) that must not be
    ///     advertised as a top-level topic.
    ///   - structNameOverride: when non-nil, overrides the default
    ///     ``swiftStructName(typeName:)`` mapping. Used to prefix cross-package collisions like
    ///     `unique_identifier_msgs/UUID` → `UniqueIdentifierUUID`.
    ///   - nestedNameOverrides: per-reference Swift-name overrides keyed by
    ///     `<package>/msg/<TypeName>`. When a nested field's full ROS name is in the map, the
    ///     mapped value wins over ``swiftStructName(typeName:)``.
    public static func emit(
        _ ir: MessageIR,
        sourceLabel: String,
        isNested: Bool = false,
        structNameOverride: String? = nil,
        nestedNameOverrides: [String: String] = [:]
    ) -> String {
        // Phase 4: pick conditional vs uniform mode. The conditional mode
        // emits a `static func typeInfo(for distro:)` factory and per-field
        // `if !encoder.isLegacySchema { … }` blocks for fields that exist on
        // the modern wire format only (e.g. `sensor_msgs/Range.variance`).
        let conditional = !isNested && isConditional(ir)
        var out = ""
        out += "// Generated by swift-ros2-gen \u{2014} DO NOT EDIT.\n"
        out += "// Source: \(sourceLabel)\n"
        out += "\n"
        out += "import SwiftROS2CDR\n"
        if conditional {
            out += "import SwiftROS2Wire\n"
        }
        out += "\n"
        out += "/// \(ir.rosTypeName)\n"
        let structName = structNameOverride ?? swiftStructName(typeName: ir.typeName)
        let conformance = isNested ? "CDRCodable, Equatable, Sendable" : "ROS2Message, Equatable, Sendable"
        out += "public struct \(structName): \(conformance) {\n"

        var preludeEmitted = false
        if !isNested {
            if conditional {
                out += emitTypeInfoFactory(ir)
                preludeEmitted = true
            } else {
                guard
                    let hashOpt = ir.perDistroHashes["jazzy"],
                    let hash = hashOpt
                else {
                    preconditionFailure(
                        "MessageIR for \(ir.rosTypeName) is missing the 'jazzy' type hash")
                }
                out += "    public static let typeInfo = ROS2MessageTypeInfo(\n"
                out += "        typeName: \"\(ir.rosTypeName)\",\n"
                out += "        typeHash: \"\(hash)\"\n"
                out += "    )\n"
                preludeEmitted = true
            }
        }

        if !ir.constants.isEmpty {
            if preludeEmitted {
                out += "\n"
            }
            for c in ir.constants {
                // ROS constants are conventionally UPPER_SNAKE; opt out of swift-format's
                // lowerCamelCase rule per-line so generated files lint cleanly.
                out += "    // swift-format-ignore: AlwaysUseLowerCamelCase\n"
                out += "    public static let \(c.swiftName): "
                out += "\(c.type.swiftTypeName) = \(swiftLiteral(c.value))\n"
            }
            preludeEmitted = true
        }

        if ir.fields.isEmpty {
            if preludeEmitted {
                out += "\n"
            }
            out += "    public init() {}\n"
            out += "\n"
            // Service halves require a single 0x00 dummy byte after the
            // encapsulation header so CycloneDDS does not collapse the sample
            // to zero length. Plain messages (.msg) keep an empty body.
            if ir.kind == .srv {
                out += "    public func encode(to encoder: CDREncoder) throws {\n"
                out += "        encoder.writeUInt8(0)\n"
                out += "    }\n"
                out += "\n"
                out += "    public init(from decoder: CDRDecoder) throws {\n"
                out += "        _ = try decoder.readUInt8()\n"
                out += "    }\n"
            } else {
                out += "    public func encode(to encoder: CDREncoder) throws {}\n"
                out += "\n"
                out += "    public init(from decoder: CDRDecoder) throws {}\n"
            }
        } else {
            if preludeEmitted {
                out += "\n"
            }
            for field in ir.fields {
                if let comment = boundedComment(field.type) {
                    out += "    \(comment)\n"
                }
                let typeName = swiftType(field.type, nestedNameOverrides: nestedNameOverrides)
                out += "    public var \(field.swiftName): \(typeName)\n"
            }
            out += "\n"
            if ir.fields.count == 1, let field = ir.fields.first {
                let typeName = swiftType(field.type, nestedNameOverrides: nestedNameOverrides)
                let defaultVal = initDefault(field, nestedNameOverrides: nestedNameOverrides)
                out += "    public init(\(field.swiftName): \(typeName) = \(defaultVal)) {\n"
                out += "        self.\(field.swiftName) = \(field.swiftName)\n"
            } else {
                out += "    public init(\n"
                for (i, field) in ir.fields.enumerated() {
                    let comma = i == ir.fields.count - 1 ? "" : ","
                    let typeName = swiftType(field.type, nestedNameOverrides: nestedNameOverrides)
                    let defaultVal = initDefault(field, nestedNameOverrides: nestedNameOverrides)
                    out += "        \(field.swiftName): \(typeName) = \(defaultVal)\(comma)\n"
                }
                out += "    ) {\n"
                for field in ir.fields {
                    out += "        self.\(field.swiftName) = \(field.swiftName)\n"
                }
            }
            out += "    }\n"
            out += "\n"
            out += "    public func encode(to encoder: CDREncoder) throws {\n"
            for field in ir.fields {
                out += emitEncodeWithAvailability(field)
            }
            out += "    }\n"
            out += "\n"
            out += "    public init(from decoder: CDRDecoder) throws {\n"
            for field in ir.fields {
                out += emitDecodeWithAvailability(
                    field, nestedNameOverrides: nestedNameOverrides)
            }
            out += "    }\n"
        }
        out += "}\n"
        return out
    }

    /// Render a `ROS2ServiceType` umbrella enum that ties the previously-emitted
    /// request / response struct halves together. The umbrella owns the canonical
    /// `<pkg>/srv/<Service>` `serviceName` plus the per-half RIHS01 hashes; the
    /// halves themselves carry their own `<pkg>/srv/<Service>_Request|Response`
    /// `typeInfo` (so they are usable as standalone messages too, which is what
    /// the wire layer hands to publishers / subscriptions internally).
    public static func emitServiceUmbrella(
        package: String,
        serviceTypeName: String,
        umbrellaName: String,
        requestStructName: String,
        responseStructName: String,
        requestHash: String?,
        responseHash: String?,
        sourceLabel: String
    ) -> String {
        var out = ""
        out += "// Generated by swift-ros2-gen \u{2014} DO NOT EDIT.\n"
        out += "// Source: \(sourceLabel)\n"
        out += "\n"
        out += "import SwiftROS2CDR\n"
        out += "\n"
        out += "/// \(package)/srv/\(serviceTypeName)\n"
        out += "public enum \(umbrellaName): ROS2ServiceType {\n"
        out += "    public static let typeInfo = ROS2ServiceTypeInfo(\n"
        out += "        serviceName: \"\(package)/srv/\(serviceTypeName)\",\n"
        out += "        requestTypeName: \"\(package)/srv/\(serviceTypeName)_Request\",\n"
        out += "        responseTypeName: \"\(package)/srv/\(serviceTypeName)_Response\",\n"
        out += "        requestTypeHash: \(swiftOptionalString(requestHash)),\n"
        out += "        responseTypeHash: \(swiftOptionalString(responseHash))\n"
        out += "    )\n"
        out += "\n"
        out += "    public typealias Request = \(requestStructName)\n"
        out += "    public typealias Response = \(responseStructName)\n"
        out += "}\n"
        return out
    }

    private static func swiftOptionalString(_ s: String?) -> String {
        guard let s = s else { return "nil" }
        return "\"\(s)\""
    }

    /// Convention: append `Msg` only when the type name collides with a Swift stdlib name
    /// (or `Empty`, kept for ROS-conventional symmetry with Phase 1). Anything else emits
    /// as bare PascalCase. All generated types coexist in the `SwiftROS2Messages` Swift
    /// module so the bare name is unambiguous.
    public static func swiftStructName(typeName: String) -> String {
        collisionTypeNames.contains(typeName) ? typeName + "Msg" : typeName
    }

    static func swiftType(_ ft: FieldType, nestedNameOverrides: [String: String] = [:]) -> String {
        switch ft {
        case .primitive(let p): return p.swiftTypeName
        case .nested(let pkg, let typeName):
            let key = "\(pkg)/msg/\(typeName)"
            return nestedNameOverrides[key] ?? swiftStructName(typeName: typeName)
        case .array(let element, _):
            return "[\(swiftType(element, nestedNameOverrides: nestedNameOverrides))]"
        case .sequence(let element, _):
            return "[\(swiftType(element, nestedNameOverrides: nestedNameOverrides))]"
        case .boundedString:
            return "String"
        }
    }

    static func swiftDefault(_ ft: FieldType, nestedNameOverrides: [String: String] = [:]) -> String {
        switch ft {
        case .primitive(let p):
            switch p {
            case .bool: return "false"
            case .byte, .uint8, .char, .int8,
                .int16, .uint16, .int32, .uint32, .int64, .uint64:
                return "0"
            case .float32, .float64: return "0.0"
            case .string, .wstring: return "\"\""
            }
        case .nested(let pkg, let typeName):
            let key = "\(pkg)/msg/\(typeName)"
            let n = nestedNameOverrides[key] ?? swiftStructName(typeName: typeName)
            return "\(n)()"
        case .array(let element, let length):
            let inner = swiftDefault(element, nestedNameOverrides: nestedNameOverrides)
            return "Array(repeating: \(inner), count: \(length))"
        case .sequence:
            return "[]"
        case .boundedString:
            return "\"\""
        }
    }

    /// Pick the init-parameter default. Prefer the parsed `defaultValue` when set; fall back
    /// to the type-shaped zero value.
    static func initDefault(_ field: FieldIR, nestedNameOverrides: [String: String]) -> String {
        if let dv = field.defaultValue, dv != .empty {
            return swiftLiteral(dv)
        }
        return swiftDefault(field.type, nestedNameOverrides: nestedNameOverrides)
    }

    /// Render a typed default as a Swift literal (no surrounding init()).
    static func swiftLiteral(_ dv: DefaultValue) -> String {
        switch dv {
        case .bool(let b): return b ? "true" : "false"
        case .int(let n): return String(n)
        case .uint(let n): return String(n)
        case .float(let d): return String(d)
        case .string(let s): return "\"\(s)\""
        case .empty: return ""
        case .array(let xs):
            return "[" + xs.map(swiftLiteral).joined(separator: ", ") + "]"
        }
    }

    /// `// note: bounded <=N` comment for bounded string / sequence fields.
    static func boundedComment(_ ft: FieldType) -> String? {
        switch ft {
        case .boundedString(_, let upper): return "// note: bounded <=\(upper)"
        case .sequence(_, let upper?): return "// note: bounded <=\(upper)"
        default: return nil
        }
    }

    /// Whether `ir` should emit in distro-conditional mode. True when any
    /// field has non-`.all` availability, OR when the IR carries multiple
    /// distros with disagreeing hash values. Multi-distro merges that only
    /// list a subset of the four canonical distros (e.g.
    /// `["humble", "jazzy"]`) still emit on the simple path — kilted /
    /// rolling are implicitly treated as same-wire-shape as jazzy.
    static func isConditional(_ ir: MessageIR) -> Bool {
        if ir.fields.contains(where: { $0.availability != .all }) { return true }
        let normalized = Set(
            ir.perDistroHashes.values.map { ($0 ?? "") }
        )
        return normalized.count > 1
    }

    /// Emit `static func typeInfo(for distro:)` plus the `static var typeInfo`
    /// shim. Splits the four `ROS2Distro` cases into the legacy (humble) arm
    /// and the modern (jazzy/kilted/rolling) arm.
    static func emitTypeInfoFactory(_ ir: MessageIR) -> String {
        var out = ""
        out += "    public static func typeInfo(for distro: ROS2Distro) -> ROS2MessageTypeInfo {\n"
        out += "        switch distro {\n"
        let humbleHash = (ir.perDistroHashes["humble"] ?? nil)
        out += "        case .humble:\n"
        out += "            return ROS2MessageTypeInfo(\n"
        out += "                typeName: \"\(ir.rosTypeName)\",\n"
        out += "                typeHash: \(quotedOrNil(humbleHash))\n"
        out += "            )\n"
        let modernHash =
            (ir.perDistroHashes["jazzy"] ?? nil)
            ?? (ir.perDistroHashes["kilted"] ?? nil)
            ?? (ir.perDistroHashes["rolling"] ?? nil)
        out += "        case .jazzy, .kilted, .rolling:\n"
        out += "            return ROS2MessageTypeInfo(\n"
        out += "                typeName: \"\(ir.rosTypeName)\",\n"
        out += "                typeHash: \(quotedOrNil(modernHash))\n"
        out += "            )\n"
        out += "        }\n"
        out += "    }\n"
        out += "\n"
        out += "    public static var typeInfo: ROS2MessageTypeInfo { typeInfo(for: .jazzy) }\n"
        return out
    }

    static func quotedOrNil(_ value: String?) -> String {
        guard let v = value, !v.isEmpty else { return "nil" }
        return "\"\(v)\""
    }

    /// Encode a field, wrapping the body in a legacy-schema guard when
    /// availability marks it as distro-conditional.
    ///
    /// Two shapes are supported:
    ///   - "modern-only" (`.onlyIn` includes `"jazzy"`): emit only when
    ///     `!encoder.isLegacySchema`, mirroring the wire shape of jazzy+.
    ///   - "legacy-only" (`.onlyIn` is `["humble"]`): emit only when
    ///     `encoder.isLegacySchema`, so a humble-shaped wire still includes
    ///     fields that were dropped in jazzy.
    static func emitEncodeWithAvailability(_ field: FieldIR) -> String {
        switch field.availability {
        case .all:
            return emitEncode(field)
        case .onlyIn(let distros):
            precondition(
                !distros.isEmpty,
                "FieldAvailability.onlyIn must contain at least one distro")
            let known: Set<String> = ["humble", "jazzy", "kilted", "rolling"]
            precondition(
                distros.allSatisfy(known.contains),
                "FieldAvailability.onlyIn contains unknown distro(s): \(distros)")
            let isLegacyOnly = distros == Set(["humble"])
            let guardCondition = isLegacyOnly ? "encoder.isLegacySchema" : "!encoder.isLegacySchema"
            let inner = emitEncode(field)
            // Indent each non-empty line by an extra 4 spaces.
            let indented =
                inner
                .split(separator: "\n", omittingEmptySubsequences: false)
                .map { line -> String in
                    line.isEmpty ? "" : "    " + String(line)
                }
                .joined(separator: "\n")
            var out = "        if \(guardCondition) {\n"
            out += indented
            out += "        }\n"
            return out
        }
    }

    /// Decode a field, branching on `decoder.isLegacySchema` when availability
    /// marks it as distro-conditional. The branch that does not contain the
    /// distro defaults the field; the other branch decodes from the wire.
    static func emitDecodeWithAvailability(
        _ field: FieldIR,
        nestedNameOverrides: [String: String]
    ) -> String {
        switch field.availability {
        case .all:
            return emitDecode(field, nestedNameOverrides: nestedNameOverrides)
        case .onlyIn(let distros):
            precondition(
                !distros.isEmpty,
                "FieldAvailability.onlyIn must contain at least one distro")
            let known: Set<String> = ["humble", "jazzy", "kilted", "rolling"]
            precondition(
                distros.allSatisfy(known.contains),
                "FieldAvailability.onlyIn contains unknown distro(s): \(distros)")
            let isLegacyOnly = distros == Set(["humble"])
            let defaultExpr = swiftDefault(
                field.type, nestedNameOverrides: nestedNameOverrides)
            let body = emitDecode(field, nestedNameOverrides: nestedNameOverrides)
            // The decode body assigns `self.<name> = ...` already. Indent it
            // under whichever branch contains the field.
            let indentedBody =
                body
                .split(separator: "\n", omittingEmptySubsequences: false)
                .map { line -> String in
                    line.isEmpty ? "" : "    " + String(line)
                }
                .joined(separator: "\n")
            var out = ""
            if isLegacyOnly {
                out += "        if decoder.isLegacySchema {\n"
                out += indentedBody
                out += "        } else {\n"
                out += "            self.\(field.swiftName) = \(defaultExpr)\n"
                out += "        }\n"
            } else {
                out += "        if decoder.isLegacySchema {\n"
                out += "            self.\(field.swiftName) = \(defaultExpr)\n"
                out += "        } else {\n"
                out += indentedBody
                out += "        }\n"
            }
            return out
        }
    }

    static func emitEncode(_ field: FieldIR) -> String {
        switch field.type {
        case .primitive:
            return "        encoder.\(writerCall(field.type))(\(field.swiftName))\n"
        case .nested:
            return "        try \(field.swiftName).encode(to: encoder)\n"
        case .array(let element, let length):
            // Fixed array: enforce caller-supplied length matches the IDL bound,
            // then per-element loop with no length prefix on the wire.
            var out = "        precondition(\n"
            out += "            \(field.swiftName).count == \(length),\n"
            out += "            \"\(field.swiftName) requires exactly \(length) elements\"\n"
            out += "        )\n"
            out += "        for v in \(field.swiftName) {\n"
            out += "            \(elementEncodeStmt(element, value: "v"))\n"
            out += "        }\n"
            return out
        case .sequence(let element, let upperBound):
            // Sequence: explicit uint32 length prefix, then per-element loop.
            // Bounded sequences enforce the IDL upper bound at encode time.
            var out = ""
            if let upper = upperBound {
                out += "        precondition(\n"
                out += "            \(field.swiftName).count <= \(upper),\n"
                out += "            \"\(field.swiftName) bounded sequence may not exceed \(upper) elements\"\n"
                out += "        )\n"
            }
            // Byte-payload fast path: a sequence of `uint8` / `byte` is the
            // largest payload in many sensor messages (e.g. `CompressedImage`,
            // `PointCloud2`). `writeUInt8Sequence` does a single `Data.append`
            // instead of a per-element loop, which is materially faster on
            // multi-megabyte buffers.
            if isUInt8Like(element) {
                out += "        encoder.writeUInt8Sequence(\(field.swiftName))\n"
                return out
            }
            out += "        encoder.writeUInt32(UInt32(\(field.swiftName).count))\n"
            out += "        for v in \(field.swiftName) {\n"
            out += "            \(elementEncodeStmt(element, value: "v"))\n"
            out += "        }\n"
            return out
        case .boundedString:
            return "        encoder.writeString(\(field.swiftName))\n"
        }
    }

    static func emitDecode(_ field: FieldIR, nestedNameOverrides: [String: String]) -> String {
        switch field.type {
        case .primitive:
            return "        self.\(field.swiftName) = try decoder.\(readerCall(field.type))()\n"
        case .nested(let pkg, let typeName):
            let key = "\(pkg)/msg/\(typeName)"
            let n = nestedNameOverrides[key] ?? swiftStructName(typeName: typeName)
            return "        self.\(field.swiftName) = try \(n)(from: decoder)\n"
        case .array(let element, let length):
            // Wrap in a `do { ... }` block so the local `out` and `count`
            // variables don't collide with peers when the message has
            // multiple array / sequence fields (e.g. sensor_msgs/Imu).
            let elemType = swiftType(element, nestedNameOverrides: nestedNameOverrides)
            let elem = elementDecodeExpr(element, nestedNameOverrides: nestedNameOverrides)
            var out = "        do {\n"
            out += "            var out: [\(elemType)] = []\n"
            out += "            out.reserveCapacity(\(length))\n"
            out += "            for _ in 0..<\(length) {\n"
            out += "                out.append(\(elem))\n"
            out += "            }\n"
            out += "            self.\(field.swiftName) = out\n"
            out += "        }\n"
            return out
        case .sequence(let element, _):
            // Byte-payload fast path mirroring the encoder: pull the entire
            // sequence with a single `Data` slice and bridge to `[UInt8]`.
            if isUInt8Like(element) {
                return "        self.\(field.swiftName) = Array(try decoder.readUInt8Sequence())\n"
            }
            let elemType = swiftType(element, nestedNameOverrides: nestedNameOverrides)
            let elem = elementDecodeExpr(element, nestedNameOverrides: nestedNameOverrides)
            var out = "        do {\n"
            out += "            let count = try decoder.readSequenceCount()\n"
            out += "            var out: [\(elemType)] = []\n"
            out += "            out.reserveCapacity(count)\n"
            out += "            for _ in 0..<count {\n"
            out += "                out.append(\(elem))\n"
            out += "            }\n"
            out += "            self.\(field.swiftName) = out\n"
            out += "        }\n"
            return out
        case .boundedString:
            return "        self.\(field.swiftName) = try decoder.readString()\n"
        }
    }

    /// Whether the element is `uint8` (or its `byte` alias). Used by the
    /// sequence-encoder fast path so byte-payload sequences (e.g. compressed
    /// image data) bypass the per-element loop.
    static func isUInt8Like(_ element: FieldType) -> Bool {
        if case .primitive(let p) = element {
            return p == .uint8 || p == .byte
        }
        return false
    }

    /// Encode a single element of an array / sequence.
    static func elementEncodeStmt(_ element: FieldType, value: String) -> String {
        switch element {
        case .primitive:
            return "encoder.\(writerCall(element))(\(value))"
        case .nested:
            return "try \(value).encode(to: encoder)"
        case .array, .sequence, .boundedString:
            preconditionFailure(
                "SwiftEmitter: nested array/sequence/boundedString elements are not supported")
        }
    }

    /// Decode a single element of an array / sequence into an `.append(...)` argument.
    static func elementDecodeExpr(_ element: FieldType, nestedNameOverrides: [String: String]) -> String {
        switch element {
        case .primitive:
            return "try decoder.\(readerCall(element))()"
        case .nested(let pkg, let typeName):
            let key = "\(pkg)/msg/\(typeName)"
            let n = nestedNameOverrides[key] ?? swiftStructName(typeName: typeName)
            return "try \(n)(from: decoder)"
        case .array, .sequence, .boundedString:
            preconditionFailure(
                "SwiftEmitter: nested array/sequence/boundedString elements are not supported")
        }
    }

    static func writerCall(_ ft: FieldType) -> String {
        switch ft {
        case .primitive(let p):
            switch p {
            case .bool: return "writeBool"
            case .byte, .uint8: return "writeUInt8"
            case .char, .int8: return "writeInt8"
            case .int16: return "writeInt16"
            case .uint16: return "writeUInt16"
            case .int32: return "writeInt32"
            case .uint32: return "writeUInt32"
            case .int64: return "writeInt64"
            case .uint64: return "writeUInt64"
            case .float32: return "writeFloat32"
            case .float64: return "writeFloat64"
            case .string, .wstring: return "writeString"
            }
        case .nested:
            preconditionFailure("writerCall is not defined for nested types — use emitEncode")
        case .array, .sequence, .boundedString:
            preconditionFailure("writerCall is not defined for array/sequence/boundedString")
        }
    }

    static func readerCall(_ ft: FieldType) -> String {
        switch ft {
        case .primitive(let p):
            switch p {
            case .bool: return "readBool"
            case .byte, .uint8: return "readUInt8"
            case .char, .int8: return "readInt8"
            case .int16: return "readInt16"
            case .uint16: return "readUInt16"
            case .int32: return "readInt32"
            case .uint32: return "readUInt32"
            case .int64: return "readInt64"
            case .uint64: return "readUInt64"
            case .float32: return "readFloat32"
            case .float64: return "readFloat64"
            case .string, .wstring: return "readString"
            }
        case .nested:
            preconditionFailure("readerCall is not defined for nested types — use emitDecode")
        case .array, .sequence, .boundedString:
            preconditionFailure("readerCall is not defined for array/sequence/boundedString")
        }
    }
}
